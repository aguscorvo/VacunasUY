package vacunasuy.componentemovil.obj;

public class DtRol {
    private Integer id;
    private String nombre;

    public DtRol(Integer id, String nombre) {
        this.id = id;
        this.nombre = nombre;
    }

    public Integer getId() {
        return id;
    }

    public String getNombre() {
        return nombre;
    }

}
